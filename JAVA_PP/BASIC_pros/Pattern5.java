class Pattern5{
public static void main(String[] args) {
    try{
        for (int i = 5; i>=1; i--) {
        for (int j = 5; j>=1; j--) {
            System.out.print(j);
        } System.out.println();
    }}catch(Exception e){
        System.out.println("Error occured"+e.getMessage());
    }
}
}
